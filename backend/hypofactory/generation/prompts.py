"""Промпты генератора гипотез. Вывод — строго на русском, JSON."""
from __future__ import annotations

SYSTEM = """Ты — эксперт-обогатитель Норникеля (флотация сульфидных медно-никелевых руд).
Твоя задача — по количественному диагнозу хвостов и подтверждающим выдержкам из
литературы формулировать КОНКРЕТНЫЕ, проверяемые в лаборатории гипотезы по
снижению потерь ценных металлов с хвостами.

Жёсткие правила:
1. Опирайся ТОЛЬКО на предоставленный диагноз (числа) и выдержки из источников.
   НЕ выдумывай реагенты, режимы или оборудование, которых нет в контексте.
2. Каждая гипотеза конкретна и проверяема: что изменить, где, ожидаемый эффект.
3. Обязательно ссылайся на источники по названию и странице из контекста.
4. Сохраняй анонимизацию металлов: пиши «Элемент 28» и «Элемент 29», не «никель/медь».
5. Различай механизмы: закрытые сростки и крупные раскрытые зёрна → измельчение/
   классификация; тонкие раскрытые зёрна (шламы) → флотация/реагенты.
6. Не предлагай гипотез по неизвлекаемому металлу (примесь в пирротине, силикаты) —
   это физический предел.
7. Если источники противоречат друг другу — не замалчивай: укажи противоречие
   в обосновании и предложи проверку, разрешающую его.
8. Пиши по-русски, деловым инженерным языком. Литература может быть на русском,
   английском или китайском — цитаты приводи в оригинале с кратким переводом."""

OUTPUT_CONTRACT = """Верни JSON-массив гипотез. Каждый элемент:
{
  "title": "краткая формулировка (до 12 слов)",
  "statement": "проверяемая гипотеза: что изменить и какой ожидается эффект",
  "rationale": "обоснование со ссылкой на числа диагноза и логику",
  "mechanism_of_influence": "физический механизм влияния на извлечение",
  "sources": [{"title": "название источника", "page": число_или_null, "quote": "короткая цитата"}],
  "risks": {"technical": ["..."], "economic": ["..."]},
  "expected_value": {"kpi_text": "ожидаемое влияние на KPI (снижение потерь), качественно/количественно"},
  "verification_roadmap": [{"step": "шаг проверки", "resources": "ресурсы", "success_criteria": "критерий успеха/провала"}],
  "confidence": число_0_1
}"""


def build_refine_prompt(hypothesis_json: str, instruction: str, context: str,
                        evidence: str, facility: str = "", goal: str = "") -> str:
    fac_block = f"\nПРОФИЛЬ ФАБРИКИ (привязывай к конкретным аппаратам):\n{facility}\n" if facility else ""
    goal_block = f"\nОБЩАЯ ЦЕЛЕВАЯ ЗАДАЧА И ОГРАНИЧЕНИЯ ТЕХНОЛОГА:\n{goal}\n" if goal else ""
    single_contract = OUTPUT_CONTRACT.replace("Верни JSON-массив гипотез. Каждый элемент:",
                                              "Верни ОДИН JSON-объект (не массив) со структурой:")
    return f"""Технолог просит ПЕРЕРАБОТАТЬ существующую гипотезу по его указанию.

ТЕКУЩАЯ ГИПОТЕЗА (JSON):
{hypothesis_json}

УКАЗАНИЕ ТЕХНОЛОГА (обязательно выполнить, не нарушая доменных правил):
{instruction}
{goal_block}
КОНТЕКСТ АНАЛИЗА:
{context}
{fac_block}
ВЫДЕРЖКИ ИЗ ИСТОЧНИКОВ (используй и цитируй):
{evidence}

Переработай гипотезу: сохрани адресуемый сигнал потерь (поток, классы), но измени
решение согласно указанию. Обнови ВСЕ поля согласованно: обоснование, механизм,
источники, риски, KPI и дорожную карту должны соответствовать новой формулировке.
{single_contract}"""


def build_user_prompt(context: str, evidence: str, known: str, n: int,
                      facility: str = "", goal: str = "", rejected: str = "") -> str:
    rej_block = ""
    if rejected:
        rej_block = f"""
ФОРМУЛИРОВКИ, РАНЕЕ ОТКЛОНЁННЫЕ ЭКСПЕРТОМ (не предлагай подобных решений):
{rejected}
"""
    goal_block = ""
    if goal:
        goal_block = f"""
ЦЕЛЕВАЯ ЗАДАЧА И ОГРАНИЧЕНИЯ ОТ ТЕХНОЛОГА (обязательно учитывай при формулировке
и отборе гипотез; если ограничение исключает какой-то класс решений — не предлагай его):
{goal}
"""
    fac_block = ""
    if facility:
        fac_block = f"""
ПРОФИЛЬ ФАБРИКИ (типовое оборудование и регламент — привязывай гипотезы к КОНКРЕТНЫМ
аппаратам и режимам из этого списка, как это делают эксперты: например «на мельницах
МШЦ 4,5×6,0…», «песковые насадки ГЦ-660…», «камеры ФПМ-16-4К контрольной флотации…»):
{facility}
"""
    return f"""ДИАГНОЗ (контекст задачи):
{context}
{goal_block}{rej_block}{fac_block}
ВЫДЕРЖКИ ИЗ ИСТОЧНИКОВ (используй и цитируй их):
{evidence}

ИЗВЕСТНЫЕ ЭКСПЕРТНЫЕ ГИПОТЕЗЫ (для оценки новизны — можно развивать, но не копируй дословно):
{known}

Сформулируй {n} наиболее ценных гипотез для этого сигнала потерь. Формулируй
инженерно конкретно: какой аппарат/параметр изменить, на сколько, где в схеме.
{OUTPUT_CONTRACT}"""
